// Helper function to get hints for a word
export const getHintsForWord = (word) => {
    return wordHints[word] || [];
};

// Helper function to get N random unique hints for a word
export const getRandomHints = (word, count) => {
    const allHints = wordHints[word] || [];
    if (allHints.length === 0) return [];
    
    const shuffled = [...allHints].sort(() => Math.random() - 0.5);
    return shuffled.slice(0, Math.min(count, allHints.length));
};

// Helper to get unique hint for each imposter
export const getUniqueHintsForImposters = (word, numImposters) => {
    const allHints = wordHints[word] || [];
    if (allHints.length < numImposters) {
        console.warn(`Not enough hints for ${word}. Need ${numImposters}, have ${allHints.length}`);
        return allHints.slice(0, numImposters);
    }
    
    const shuffled = [...allHints].sort(() => Math.random() - 0.5);
    return shuffled.slice(0, numImposters);
};